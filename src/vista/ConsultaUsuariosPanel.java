    public void llenarTablaUsuarios(){
        UsuarioLogica usuarioLogica = new UsuarioLogica();
        List<Usuario> listaUsuarios = usuarioLogica.listarUsuariosPorFiltro(programaComboBox.getSelectedItem().toString(), 
                                                                            rolComboBox.getSelectedItem().toString(),
                                                                            semestreComboBox.getSelectedItem().toString());
        campoCantidadElementos.setText(String.valueOf(listaUsuarios.size()));
        campoCantidadElementos.setEditable(false);
        setTabla(listaUsuarios);
    }
    
    public void setTabla(List<Usuario> usuarios){
        modelo =  (DefaultTableModel) consultaIngresoUsurTabla.getModel();
        int size = modelo.getRowCount();
        for(int i=0; i < size; i++){
            modelo.removeRow(0);
        }
        String aux[];
        for(int j=0;j< usuarios.size();j++){
            aux = new String[6];
            aux[0] = usuarios.get(j).getUsuarioID().toString();
            aux[1] = usuarios.get(j).getNombreUsuario();
            aux[2] = usuarios.get(j).getRol();
            aux[3] = String.valueOf(usuarios.get(j).getSemestre());
            aux[4] = String.valueOf(usuarios.get(j).getProgramaAcademico());
            aux[5] = usuarios.get(j).getEmail();
                    
            modelo.addRow(aux);
        }
    }    
    
    
    
