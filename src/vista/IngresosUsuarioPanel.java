    private void botonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonConsultarActionPerformed
        String codigoUsuario = campoCodigoUsuario.getText();
        Usuario usuario = new Usuario();
        UsuarioLogica usuarioLogica = new UsuarioLogica();
        
        if(campoCodigoUsuario.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "El campo no debe estar vacio");
        }
        else{
            try{
                usuario = usuarioLogica.buscarUsuario(Integer.valueOf(codigoUsuario));
                campoUsuario.setText(usuario.getUsuarioID()+" - "+usuario.getNombreUsuario()+" - "+usuario.getRol()+" - "+usuario.getProgramaAcademico()+" - Sem: "+usuario.getSemestre());
                campoUsuario.setEditable(false);
                llenarTablaIngresos(usuario);
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, "No se encontro el Usuario");
            }
            
            
        }
        
            
        
        
        
    }//GEN-LAST:event_botonConsultarActionPerformed

