    public void setTabla(List<Prestamo> prestamos){
        modelo =  (DefaultTableModel) PrestamosTabla.getModel();
        
        int size = modelo.getRowCount();
        for(int i=0; i < size; i++){
            modelo.removeRow(0);
        }
        String aux[];
        for(int j=0;j< prestamos.size();j++){
            aux = new String[10];
            aux[0] = String.valueOf(prestamos.get(j).getEquipoID().getEquipoID());
            aux[1] = prestamos.get(j).getEquipoID().getDescripcion();
            aux[2] = prestamos.get(j).getFechaEntrega().toString();
            aux[3] = prestamos.get(j).getHoraEntrega();
            aux[4] = prestamos.get(j).getFechaEntrega().toString();
            aux[5] = prestamos.get(j).getHoraDevolucion();
            aux[6] = prestamos.get(j).getNombreMonitorAtendio();
            aux[7] = prestamos.get(j).getNombreMonitorDevolvio();
            aux[8] = prestamos.get(j).getObservaciones();
            aux[9] = prestamos.get(j).getEstadoPrestamo();
            modelo.addRow(aux);
        }
    }    

