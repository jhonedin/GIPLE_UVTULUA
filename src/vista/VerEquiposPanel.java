    public void llenarTablaPrestamos(){
        equipoLogica = new EquipoLogica();
        List<Equipo> listaE = equipoLogica.listarEquiposPorFiltro(tipoComboBox.getSelectedItem().toString(), 
                                                                            estadoComboBox.getSelectedItem().toString());
        campoCantidad.setText(String.valueOf(listaE.size()));
        setTabla(listaE);
    }     
    
public void setTabla(List<Equipo> equipos){
        modelo =  (DefaultTableModel) PrestamosTabla.getModel();
        
        int size = modelo.getRowCount();
        for(int i=0; i < size; i++){
            modelo.removeRow(0);
        }
        String aux[];
        for(int j=0;j< equipos.size();j++){
            aux = new String[12];
            aux[0] = String.valueOf(equipos.get(j).getEquipoID());
            aux[1] = equipos.get(j).getDescripcion();
            try{
                aux[2] = equipos.get(j).getFechaIngreso().toString();
            }catch(Exception e){
                aux[2] = "No fecha Ingreso";
            }
            try{
                aux[3] = equipos.get(j).getFechaUltimaRevision().toString();
            }catch(Exception e){
                aux[3] = "No fecha Revision";
            }
            
            aux[4] = equipos.get(j).getTipoEquipo().toString();
            aux[5] = String.valueOf(equipos.get(j).getHorasUso());
            aux[6] = equipos.get(j).getEstado();
            modelo.addRow(aux);
        }
    }    

