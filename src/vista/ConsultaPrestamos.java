    public void setTabla(List<Prestamo> prestamos){
        modelo =  (DefaultTableModel) PrestamosTabla.getModel();
        
        int size = modelo.getRowCount();
        for(int i=0; i < size; i++){
            modelo.removeRow(0);
        }
        String aux[];
        for(int j=0;j< prestamos.size();j++){
            aux = new String[14];
            aux[0] = String.valueOf(prestamos.get(j).getPrestamoID());
            aux[1] = String.valueOf(prestamos.get(j).getEquipoID().getEquipoID());
            aux[2] = prestamos.get(j).getEquipoID().getDescripcion();
            
            aux[3] = String.valueOf(prestamos.get(j).getUsuarioID().getUsuarioID());
            aux[4] = prestamos.get(j).getUsuarioID().getNombreUsuario();
            
            
            aux[5] = prestamos.get(j).getFechaEntrega().toString();
            aux[6] = prestamos.get(j).getHoraEntrega();
            try{
                aux[7] = prestamos.get(j).getFechaDevolucion().toString();
            }catch(Exception e){
                aux[7] = "No fecha";
            }
            aux[8] = prestamos.get(j).getHoraDevolucion();
            aux[9] = prestamos.get(j).getNombreMonitorAtendio();
            aux[10] = prestamos.get(j).getNombreMonitorDevolvio();
            aux[11] = prestamos.get(j).getEstadoPrestamo();
            aux[12] = prestamos.get(j).getObservaciones();
            aux[13] = prestamos.get(j).getAsignatura();
            modelo.addRow(aux);
        }
    }        

    
    

